package Polymorphism;

public class Overloading {
	
	public void division() {
		int a = 80;
		int b = 4;
		System.out.println(a/b);
	}
	public void division(int a) {
		int b = 5;
		System.out.println(a/b);
		
	}
	public void division(int a, int b) {
		System.out.println(a/b);
	}
	

}
