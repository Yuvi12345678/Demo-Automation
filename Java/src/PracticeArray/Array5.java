package PracticeArray;

public class Array5 {
	public static void main(String[] args) {
		int arr[] = {49,48,29,50,38,39,37,89};
		
		
		for(int i=0; i<=arr.length-1; i++) {
			System.out.println(arr[i]);
			
		}
		
		for(int i=arr.length-1; i>=0; i--) {
			System.out.println(arr[i]);
		}
	}

}
